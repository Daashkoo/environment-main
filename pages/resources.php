                    <li><strong>Support Sustainable Brands:</strong> Choose products from companies that prioritize sustainable practices, eco-friendly materials, and ethical sourcing.</li>
                    <li><strong>Get Involved:</strong> Join local environmental groups, advocate for policy changes, and participate in community projects focused on sustainability.</li>
                </ul>
